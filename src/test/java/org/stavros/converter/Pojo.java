package org.stavros.converter;

public class Pojo {
	
	private int a;
	
	private long b;
	
	private float c;
	
	private double d;
	
	private char e;
	
	private String string;
	
	private Nested nested;

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public long getB() {
		return b;
	}

	public void setB(long b) {
		this.b = b;
	}

	public float getC() {
		return c;
	}

	public void setC(float c) {
		this.c = c;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}

	public char getE() {
		return e;
	}

	public void setE(char e) {
		this.e = e;
	}

	public String getString() {
		return string;
	}

	public void setString(String string) {
		this.string = string;
	}

	public Nested getNested() {
		return nested;
	}

	public void setNested(Nested nested) {
		this.nested = nested;
	}
	

}
